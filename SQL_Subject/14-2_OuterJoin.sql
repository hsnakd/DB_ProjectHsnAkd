--LEFT OUTER
SELECT CUSTOMER_ID,FIRST_NAME,LAST_NAME,ADDRESS,PHONE
FROM CUSTOMER LEFT OUTER JOIN ADDRESS      --OUTER is optional ==> LEFT == LEFT OUTER
ON CUSTOMER.ADDRESS_ID = ADDRESS.ADDRESS_ID;

---- return department name and belong state/province in the same table
SELECT DEPARTMENT_NAME, STATE_PROVINCE
FROM DEPARTMENTS LEFT OUTER JOIN LOCATIONS
ON DEPARTMENTS.LOCATION_ID = LOCATIONS.LOCATION_ID ;

-- show employee first name and their start date
SELECT FIRST_NAME, START_DATE
FROM JOB_HISTORY LEFT OUTER JOIN EMPLOYEES
ON JOB_HISTORY.EMPLOYEE_ID = EMPLOYEES.EMPLOYEE_ID;

SELECT DEPARTMENT_NAME, END_DATE
FROM DEPARTMENTS D LEFT JOIN JOB_HISTORY J
ON D.DEPARTMENT_ID = J.JOB_ID;

-- get me job id and job title together
SELECT E.JOB_ID, JOB_TITLE
FROM EMPLOYEES E LEFT JOIN JOBS J
ON E.JOB_ID = J.JOB_ID;

-- get me department names and end date
-- use above statement, eliminate null values using where
SELECT DEPARTMENT_NAME, END_DATE
FROM DEPARTMENTS LEFT JOIN JOB_HISTORY
ON DEPARTMENTS.DEPARTMENT_ID = JOB_HISTORY.DEPARTMENT_ID
WHERE END_DATE IS NOT NULL;


