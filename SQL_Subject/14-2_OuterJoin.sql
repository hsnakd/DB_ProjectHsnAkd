--FULL OUTER
SELECT CUSTOMER_ID,FIRST_NAME,LAST_NAME,ADDRESS,PHONE
FROM CUSTOMER FULL JOIN ADDRESS      --OUTER is optional ==> FULL == FULL OUTER
ON CUSTOMER.ADDRESS_ID = ADDRESS.ADDRESS_ID;

-- show region name and country name together
SELECT REGION_ID, COUNTRY_NAME
FROM REGIONS FULL JOIN COUNTRIES
ON REGIONS.REGION_ID = COUNTRIES.REGION_ID;

-- get me first name , start date, end date in same table of all employees
SELECT FIRST_NAME, START_DATE, END_DATE
FROM EMPLOYEES FULL JOIN JOB_HISTORY
ON EMPLOYEES.EMPLOYEE_ID = JOB_HISTORY.EMPLOYEE_ID;



